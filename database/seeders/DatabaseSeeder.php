    /**
     * Create a user with given role
     *
     * @param $role
     */
    private function createUser($role)
    {
        $name = strtolower($role);
        
        // Create user using factory
        $user = User::factory()->create([
            'username' => $name,
            'first_name' => ucfirst($name),
            'middle_name' => '',
            'last_name' => ucfirst($role),
            'email' => $name . '@gmail.com',
            'type' => ($role === 'SuperAdmin') ? 1 : 0,
            'password' => Hash::make('12345678'),
            'status' => 1
        ]);

        // Assign role
        $user->assign($role);

        // If it's superadmin, assign all abilities
        if ($role === 'SuperAdmin') {
            foreach (Ability::all() as $ability) {
                BouncerFacade::allow($user)->to($ability->name);
            }
        }

        $this->command->info("Created user for role: {$role}");
        
        return $user->id;
    }

